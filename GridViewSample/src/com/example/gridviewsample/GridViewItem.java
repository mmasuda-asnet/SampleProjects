package com.example.gridviewsample;

public class GridViewItem {
	private int itemId;
	private String name;
	
	public void setItemId(int itemId) {
		this.itemId = itemId;
	}
	public int getItemId() {
		return itemId;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
}
